// Listing 13-22: Using iterator adaptor methods in the implementation of the search function
pub fn search<'a>(query: &str, contents: &'a str) -> Vec<&'a str> {
    contents
        .lines()
        .filter(|line| line.contains(query))
        .collect()
}




